    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        //REMOVER
        String id = textoRemov.getText();
        int idConvertido = Integer.parseInt(id);
        Funcionario funcionario = daoFuncionario.localizar(idConvertido);
        if (funcionario != null) {
            String nomeTemp = funcionario.get_nome();
            String sobrenomeTemp = funcionario.get_sobrenome();
            daoFuncionario.remover(funcionario);
            JOptionPane.showMessageDialog(null, "Funcionario "+ nomeTemp + " " + sobrenomeTemp  +" removido \n");
        } else {
             JOptionPane.showMessageDialog(null, "Funcionario não encontrado! \n");
        }


        atualizaTabelaFuncionarios();
    }//GEN-LAST:event_jButton8ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Botão Cadastro
        String nome = textoNome.getText();
        String sobre =textSobre.getText();
        
       
        int matricula = Integer.parseInt(textMatr.getText());
        
        Funcionario novoFuncionario = new Funcionario(0,nome,sobre,matricula);
        JOptionPane.showMessageDialog(null, "Funcionario Cadastrado com id "+ (novoFuncionario.get_id()+1));
        daoFuncionario.incluir(novoFuncionario);
        
        atualizaTabelaFuncionarios();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void buttVisuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttVisuActionPerformed
        //Localizar funcionario
        String id = textoLoca.getText();
        int idConvertido;
       
        try {
            idConvertido = Integer.parseInt(id);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Insira um número de ID válido.");
        return; // Encerra o método em caso de erro de conversão
        }
        Funcionario funcionario = daoFuncionario.localizar(idConvertido);
        if (funcionario != null) {
            String funcio = funcionario.toString();
            JOptionPane.showMessageDialog(null, "Informações do Funcionário:\n" + funcio);
        } else {
            JOptionPane.showMessageDialog(null, "Funcionário não encontrado!");
        }


        
    }//GEN-LAST:event_buttVisuActionPerformed
    public void atualizaTabelaFuncionarios(){
        listFuncionarios.clear();
        listFuncionarios.addAll(daoFuncionario.getLista());
    }
