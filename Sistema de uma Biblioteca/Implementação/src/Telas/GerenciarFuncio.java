    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    //Atualizar
        String id = textoID.getText();
        int idConvertido = Integer.parseInt(id);
        Funcionario funcionario = daoFuncionario.localizar(idConvertido);
        if (funcionario != null) {
            telaAtualizarFuncio = new Atualizar(funcionario);
        } else {
             JOptionPane.showMessageDialog(null, "Funcionario não encontrado! \n");
        }

    
    atualizaTabelaFuncionarios();
    }//GEN-LAST:event_jButton2ActionPerformed

